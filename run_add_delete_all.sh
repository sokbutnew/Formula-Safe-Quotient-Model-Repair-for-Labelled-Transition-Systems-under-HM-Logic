    print("cuda_device =", torch.cuda.get_device_name(0))
PY
PREPARED_DIR="${PREPARED_DIR}" RESULTS_ROOT="${RESULTS_ROOT}" DEVICE="${DEVICE}" STRICT_DEVICE="${STRICT_DEVICE}" \
TARGET_STATE="${TARGET_STATE}" \
