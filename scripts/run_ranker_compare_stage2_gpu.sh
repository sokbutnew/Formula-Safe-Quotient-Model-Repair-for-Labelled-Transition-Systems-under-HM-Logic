#!/usr/bin/env bash
set -euo pipefail

# Stage-2-only linear-ranker ablation. This assumes Stage 1 has already
# produced PREPARED_DIR. It does not run MLP/GNN.
#
# Compared variants:
#   legacy8_ablation             linear/legacy_v3, 8 candidate features
#   lightweight_contextual_linear linear/current, 27 contextual features

PREPARED_DIR="${PREPARED_DIR:-results/add_delete_prepared_gpu_all}"
RESULTS_ROOT="${RESULTS_ROOT:-results/ranker_compare_stage2}"
DEVICE="${DEVICE:-cuda}"
STRICT_DEVICE="${STRICT_DEVICE:-1}"
V_SIZES="${V_SIZES:-0,1,3,5}"
FORMULA_LIMIT="${FORMULA_LIMIT:-0}"
TARGET_STATE="${TARGET_STATE:--1}"
COMPARE_BUDGET="${COMPARE_BUDGET:-baseline}"

if [[ "${COMPARE_BUDGET}" == "relaxed" ]]; then
  DEFAULT_RANKER_TRAIN_SAMPLES=30000
  DEFAULT_RANKER_TRAIN_CANDIDATE_LIMIT=256
  DEFAULT_MAX_ITERS=24
  DEFAULT_CANDIDATE_LIMIT=256
  DEFAULT_CANDIDATE_STATE_LIMIT=512
  DEFAULT_STATE_SCAN_LIMIT=20000
  DEFAULT_MINIMAL_LAYER_WIDTH=8192
elif [[ "${COMPARE_BUDGET}" == "baseline" ]]; then
  DEFAULT_RANKER_TRAIN_SAMPLES=8000
  DEFAULT_RANKER_TRAIN_CANDIDATE_LIMIT=64
  DEFAULT_MAX_ITERS=16
  DEFAULT_CANDIDATE_LIMIT=64
  DEFAULT_CANDIDATE_STATE_LIMIT=128
  DEFAULT_STATE_SCAN_LIMIT=5000
  DEFAULT_MINIMAL_LAYER_WIDTH=2048
else
  echo "ERROR: COMPARE_BUDGET must be baseline or relaxed. Current COMPARE_BUDGET=${COMPARE_BUDGET}" >&2
  exit 2
fi

RANKER_TRAIN_SAMPLES="${RANKER_TRAIN_SAMPLES:-${DEFAULT_RANKER_TRAIN_SAMPLES}}"
RANKER_TRAIN_FORMULA_LIMIT="${RANKER_TRAIN_FORMULA_LIMIT:-30}"
RANKER_TRAIN_CANDIDATE_LIMIT="${RANKER_TRAIN_CANDIDATE_LIMIT:-${DEFAULT_RANKER_TRAIN_CANDIDATE_LIMIT}}"
RANKER_EPOCHS="${RANKER_EPOCHS:-40}"
RANKER_LR="${RANKER_LR:-0.003}"

BEAM_WIDTH="${BEAM_WIDTH:-4}"
MAX_ITERS="${MAX_ITERS:-${DEFAULT_MAX_ITERS}}"
CANDIDATE_LIMIT="${CANDIDATE_LIMIT:-${DEFAULT_CANDIDATE_LIMIT}}"
CANDIDATE_STATE_LIMIT="${CANDIDATE_STATE_LIMIT:-${DEFAULT_CANDIDATE_STATE_LIMIT}}"
STATE_SCAN_LIMIT="${STATE_SCAN_LIMIT:-${DEFAULT_STATE_SCAN_LIMIT}}"
MINIMAL_LAYER_WIDTH="${MINIMAL_LAYER_WIDTH:-${DEFAULT_MINIMAL_LAYER_WIDTH}}"
MINIMAL_SEEN_LIMIT="${MINIMAL_SEEN_LIMIT:-1000000}"
DRIFT_MODE="${DRIFT_MODE:-estimate}"
EXACT_DRIFT_MAX_TRANSITIONS="${EXACT_DRIFT_MAX_TRANSITIONS:-200000}"
STAGE2_MAX_STATES="${STAGE2_MAX_STATES:-1000000}"
STAGE2_MAX_TRANSITIONS="${STAGE2_MAX_TRANSITIONS:-5000000}"
STAGE2_PROGRESS_EVERY="${STAGE2_PROGRESS_EVERY:-1}"
STAGE2_CASE_PROGRESS_EVERY="${STAGE2_CASE_PROGRESS_EVERY:-0}"
TRIM_MEMORY_EVERY_CASE="${TRIM_MEMORY_EVERY_CASE:-1}"
CACHE_QUOTIENT_MODELS="${CACHE_QUOTIENT_MODELS:-0}"
FORCE_RANKER_TRAIN="${FORCE_RANKER_TRAIN:-1}"
RUN_LEGACY8_ABLATION="${RUN_LEGACY8_ABLATION:-1}"
RUN_CONTEXTUAL_LINEAR="${RUN_CONTEXTUAL_LINEAR:-1}"
LEGACY8_NAME="${LEGACY8_NAME:-legacy8_ablation}"
CONTEXTUAL_NAME="${CONTEXTUAL_NAME:-lightweight_contextual_linear}"

if [[ ! -f "${PREPARED_DIR}/manifest.json" ]]; then
  echo "ERROR: ${PREPARED_DIR}/manifest.json not found. Run Stage 1 first." >&2
  exit 2
fi

mkdir -p "${RESULTS_ROOT}" models

run_one() {
  local name="$1"
  local linear_feature_set="$2"
  local model_path="$3"
  local out_dir="${RESULTS_ROOT}/${name}"

  echo "== Stage 2 linear-ranker ablation: ${name} (${linear_feature_set}) =="
  PREPARED_DIR="${PREPARED_DIR}" RESULTS_ROOT="${out_dir}" DEVICE="${DEVICE}" STRICT_DEVICE="${STRICT_DEVICE}" \
  RANKER_MODEL="${model_path}" V_SIZES="${V_SIZES}" FORMULA_LIMIT="${FORMULA_LIMIT}" TARGET_STATE="${TARGET_STATE}" \
  RANKER_TRAIN_SAMPLES="${RANKER_TRAIN_SAMPLES}" RANKER_TRAIN_FORMULA_LIMIT="${RANKER_TRAIN_FORMULA_LIMIT}" \
  RANKER_TRAIN_CANDIDATE_LIMIT="${RANKER_TRAIN_CANDIDATE_LIMIT}" RANKER_EPOCHS="${RANKER_EPOCHS}" \
  RANKER_LR="${RANKER_LR}" RANKER_ARCHITECTURE="linear" LINEAR_FEATURE_SET="${linear_feature_set}" \
  DEFAULT_RANKER="neural" INCLUDE_NEURAL="1" INCLUDE_HEURISTIC_COMPARISON="0" FORCE_RANKER_TRAIN="${FORCE_RANKER_TRAIN}" \
  BEAM_WIDTH="${BEAM_WIDTH}" MAX_ITERS="${MAX_ITERS}" CANDIDATE_LIMIT="${CANDIDATE_LIMIT}" \
  CANDIDATE_STATE_LIMIT="${CANDIDATE_STATE_LIMIT}" STATE_SCAN_LIMIT="${STATE_SCAN_LIMIT}" \
  SEARCH_STRATEGY="${SEARCH_STRATEGY:-beam}" EXPERIMENT_PROFILE="ranker-add-delete" \
  MINIMAL_LAYER_WIDTH="${MINIMAL_LAYER_WIDTH}" MINIMAL_SEEN_LIMIT="${MINIMAL_SEEN_LIMIT}" \
  DRIFT_MODE="${DRIFT_MODE}" EXACT_DRIFT_MAX_TRANSITIONS="${EXACT_DRIFT_MAX_TRANSITIONS}" \
  STAGE2_MAX_STATES="${STAGE2_MAX_STATES}" STAGE2_MAX_TRANSITIONS="${STAGE2_MAX_TRANSITIONS}" \
  STAGE2_PROGRESS_EVERY="${STAGE2_PROGRESS_EVERY}" STAGE2_CASE_PROGRESS_EVERY="${STAGE2_CASE_PROGRESS_EVERY}" \
  TRIM_MEMORY_EVERY_CASE="${TRIM_MEMORY_EVERY_CASE}" CACHE_QUOTIENT_MODELS="${CACHE_QUOTIENT_MODELS}" \
  bash scripts/run_add_delete_stage2_gpu.sh
}

if [[ "${RUN_LEGACY8_ABLATION}" == "1" ]]; then
  run_one "${LEGACY8_NAME}" "legacy_v3" "models/add_delete_ranker_linear_legacy8.pt"
fi
if [[ "${RUN_CONTEXTUAL_LINEAR}" == "1" ]]; then
  run_one "${CONTEXTUAL_NAME}" "current" "models/add_delete_ranker_lightweight_contextual_linear.pt"
fi

if [[ "${RUN_LEGACY8_ABLATION}" == "1" && "${RUN_CONTEXTUAL_LINEAR}" == "1" ]]; then
  python -m svbr.experiments.compare_rankers \
    --results-root "${RESULTS_ROOT}" \
    --left "${LEGACY8_NAME}" \
    --right "${CONTEXTUAL_NAME}" \
    --output-prefix "legacy8_ablation_vs_contextual_linear_stage2"
else
  echo "Stage-2 comparison report skipped because one side was disabled."
fi
